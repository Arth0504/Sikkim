import CustomReservation from "../models/CustomReservation.js";
import Package from "../models/Package.js";
import { createAdminNotification } from "../utils/adminNotificationHelper.js";

// CREATE RESERVATION REQUEST (User)
export const createReservation = async (req, res) => {
  try {
    const { budget, duration, interests, region, aiGeneratedPlan, estimatedPrice } = req.body;

    if (!budget || !duration || !interests || !region || !aiGeneratedPlan || !estimatedPrice) {
      return res.status(400).json({ message: "All reservation fields are required." });
    }

    const reservation = await CustomReservation.create({
      user: req.user._id,
      budget,
      duration,
      interests,
      region,
      aiGeneratedPlan,
      estimatedPrice,
      status: "Pending",
    });

    // Auto-create Admin Notification
    await createAdminNotification({
      title: "New Custom Tour Request 🗺️",
      message: `User ${req.user.name || "Customer"} requested a custom ${interests} tour for ${region} (Budget: ${budget}, Est: ₹${estimatedPrice}).`,
      type: "CUSTOM_RESERVATION",
      referenceId: reservation._id.toString(),
      actionUrl: "/admin/custom-requests",
    });

    return res.status(201).json({
      success: true,
      message: "Custom Tour Reservation requested successfully ✅",
      reservation,
    });
  } catch (error) {
    console.error("CREATE RESERVATION ERROR:", error);
    return res.status(500).json({ message: error.message });
  }
};

// GET USER RESERVATIONS (User)
export const getUserReservations = async (req, res) => {
  try {
    const reservations = await CustomReservation.find({ user: req.user._id })
      .populate("customPackage")
      .populate("booking")
      .sort({ createdAt: -1 });

    return res.status(200).json(reservations);
  } catch (error) {
    console.error("GET USER RESERVATIONS ERROR:", error);
    return res.status(500).json({ message: error.message });
  }
};

// GET RESERVATION BY ID
export const getReservationById = async (req, res) => {
  try {
    const reservation = await CustomReservation.findById(req.params.id)
      .populate("user", "name email")
      .populate("customPackage")
      .populate("booking");

    if (!reservation) {
      return res.status(404).json({ message: "Reservation not found." });
    }

    return res.status(200).json(reservation);
  } catch (error) {
    console.error("GET RESERVATION BY ID ERROR:", error);
    return res.status(500).json({ message: error.message });
  }
};

