// UPDATE RESERVATION STATUS / MODIFY DETAILS (Admin)
export const updateReservationStatus = async (req, res) => {
  try {
    const { id } = req.params;
    const { status, adminRemarks, estimatedPrice, aiGeneratedPlan } = req.body;

    const reservation = await CustomReservation.findById(id);
    if (!reservation) {
      return res.status(404).json({ message: "Reservation not found." });
    }

    if (adminRemarks !== undefined) reservation.adminRemarks = adminRemarks;
    if (estimatedPrice !== undefined) reservation.estimatedPrice = estimatedPrice;
    if (aiGeneratedPlan !== undefined) reservation.aiGeneratedPlan = aiGeneratedPlan;
    if (status !== undefined) reservation.status = status;

    // If status changed to Approved, generate the Package document
    if (status === "Approved" && !reservation.customPackage) {
      const plan = reservation.aiGeneratedPlan;
      const customPkg = await Package.create({
        name: plan.name || `AI Custom: ${reservation.region} Tour`,
        description: plan.description || `Custom package generated by AI suggestion.`,
        price: reservation.estimatedPrice,
        duration: plan.duration || `${plan.itinerary?.length || 4} Days`,
        itinerary: plan.itinerary || [],
        region: reservation.region,
        isActive: false, // Keep out of public packages page
        isCustom: true,
      });

      reservation.customPackage = customPkg._id;
    }

    await reservation.save();

    return res.status(200).json({
      success: true,
      message: `Reservation request status updated to ${status} ✅`,
      reservation,
    });
  } catch (error) {
    console.error("UPDATE RESERVATION ERROR:", error);
    return res.status(500).json({ message: error.message });
  }
};
